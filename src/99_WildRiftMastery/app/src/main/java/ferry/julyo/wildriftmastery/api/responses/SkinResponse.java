package ferry.julyo.wildriftmastery.api.responses;



public class SkinResponse {
    private String id;
    private String name;
    private String num;

    public SkinResponse(){
    }

    public String getId() {
        return id;
    }

    public void setId(String value) {
        this.id = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String value) {
        this.name = value;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }
}

