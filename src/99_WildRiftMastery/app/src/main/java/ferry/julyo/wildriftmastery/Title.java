package ferry.julyo.wildriftmastery;

public class Title {
    private String btnChampName;
    private String btnItemName;
    private String btnVoiceName;
    private String btnWallName;

    public Title() {
        this.btnChampName = btnChampName;
        this.btnItemName = btnItemName;
        this.btnVoiceName = btnVoiceName;

    }

    public String getBtnVoiceName() {
        return btnVoiceName;
    }

    public void setBtnVoiceName(String btnVoiceName) {
        this.btnVoiceName = btnVoiceName;
    }

    public String getBtnItemName() {
        return btnItemName;
    }

    public void setBtnItemName(String btnItemName) {
        this.btnItemName = btnItemName;
    }

    public String getBtnChampName() {
        return btnChampName;
    }

    public void setBtnChampName(String btnChampName) {
        this.btnChampName = btnChampName;
    }

    public String getBtnWallName() {
        return btnWallName;
    }

    public void setBtnWallName(String btnWallName) {
        this.btnWallName = btnWallName;
    }
}

